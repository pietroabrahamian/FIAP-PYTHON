# (a) Mostre a soma x+y
n1 = int(input("Me fale um número:"))
n2 = int(input("Me fale outro número:"))
soma = n1 + n2
print(f"A soma desses números é: {soma}")

# (b) Mostre mais 3 operações à sua escolha
